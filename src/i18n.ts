const dicts: Partial<Record<Lang, Dict>> = { en, my }

let currentLang: Lang = 'en'
const subscribers = new Set<(l: Lang) => void>()

function readStored(): Lang {
  if (typeof window === 'undefined') return 'en'
  const v = localStorage.getItem(KEY) as Lang | null
  return v && (v === 'en' || v === 'my' || v === 'zh' || v === 'th' || v === 'ja' || v === 'ko') ? v : 'en'
}

currentLang = readStored()

export function useLang() {
  const [lang, setLangState] = useState<Lang>(currentLang)

  useEffect(() => {
    const onChange = (l: Lang) => setLangState(l)
    subscribers.add(onChange)
    return () => { subscribers.delete(onChange) }
  }, [])

  const setLang = (next: Lang) => {
    currentLang = next
    if (typeof window !== 'undefined') localStorage.setItem(KEY, next)
    subscribers.forEach((fn) => fn(next))
  }

  return { lang, setLang }
}

export function useT() {
  const { lang } = useLang()
  return (key: string, vars?: Record<string, string | number>): string => {
    const dict = dicts[lang] ?? en
    const raw = dict[key] ?? en[key] ?? key
    if (!vars) return raw
    return Object.keys(vars).reduce((s, k) => s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(vars[k])), raw)
  }
}
